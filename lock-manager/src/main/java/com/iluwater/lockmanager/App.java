package com.iluwater.lockmanager;


/**
 * Embedded Value pattern is used when the variables inside the nested class should be updated
 * separately for the database.
 */
public class App {

  /**
   * Program entry point.
   *
   * @param args command line args
   */

  public static void main(String[] args)  {
    LockManager lockManager = LockManager.getLockManager("PROCESSES");
    Process process1 = new Process(1);
    Process process2 = new Process(2);

    System.out.println("User 1, Obj1: " + lockManager.requestLock("user1", process1.getUniqueId()));
    System.out.println("User 2, Obj1: " + lockManager.requestLock("user2", process1.getUniqueId()));
    System.out.println("User 2, Obj2: " + lockManager.requestLock("user2", process2.getUniqueId()));
    System.out.println("Release Obj1  " + lockManager.releaseLock(process1.getUniqueId()));
    System.out.println("User 2, Obj1: " + lockManager.requestLock("user2", process1.getUniqueId()));
  }
}